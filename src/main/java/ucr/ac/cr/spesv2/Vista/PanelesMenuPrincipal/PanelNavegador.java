    public void cambiarBotones(String estado){
        switch(estado){
            case "Producciones":
                btnProducciones.setIcon(new ImageIcon());
                btnEventos.setIcon(new ImageIcon());
                btnUsuarios.setIcon(new ImageIcon());
            break;
            
            case "Eventos":
                btnProducciones.setIcon(new ImageIcon());
                btnEventos.setIcon(new ImageIcon());
                btnUsuarios.setIcon(new ImageIcon());
            break;
            
            case "Usuarios":
                btnProducciones.setIcon(new ImageIcon());
                btnEventos.setIcon(new ImageIcon());
                btnUsuarios.setIcon(new ImageIcon());
            break;
        }//Fin switch
    }//Fin cambiarBotones
    
